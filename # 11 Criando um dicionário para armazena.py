# 11 Criando um dicionário para armazenar informações sobre uma pessoa
pessoa = {
    "primeiro_nome": "Ridael",
    "sobrenome": "Terceiro",
    "idade": 22,
    "cidade": "João Pessoa"
}

# Exibindo as informações armazenadas no dicionário
print("Informações da pessoa:")
print(f"Primeiro Nome: {pessoa['primeiro_nome']}")
print(f"Sobrenome: {pessoa['sobrenome']}")
print(f"Idade: {pessoa['idade']}")
print(f"Cidade: {pessoa['cidade']}")